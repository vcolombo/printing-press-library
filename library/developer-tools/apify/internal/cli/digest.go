// Newsletter digest renderer.
//
// Pulls items from the local pp_dataset_items table within a time window,
// dedupes by URL + title-similarity, ranks by (engagement + recency + novelty),
// fills a Go text/template. Output is markdown ready for Beehiiv/Ghost paste
// or to pipe into a writer agent.
//
//	apify-pp-cli digest --topic "AI dev tools" --since 24h --template default
//	apify-pp-cli digest --topic AI --since 7d --template-file ./tiktok.tmpl
//	apify-pp-cli digest --offline --topic AI --since 30d --limit 10
package cli

import (
	"context"
	"encoding/json"
	"fmt"
	"math"
	"os"
	"sort"
	"strings"
	"text/template"
	"time"

	"github.com/spf13/cobra"

	"github.com/mvanhorn/printing-press-library/library/developer-tools/apify/internal/store"
)

// Built-in digest templates. Users can override with --template-file.
const defaultDigestTemplate = `# {{.Topic}} — {{.Window}}

{{range .Items}}- **[{{.Title}}]({{.URL}})** — {{.SourceActor}}{{if .Author}} · _{{.Author}}_{{end}}{{if .PublishedAtPretty}} · {{.PublishedAtPretty}}{{end}}
{{if .Body}}  {{.Body}}
{{end}}
{{end}}
_Generated by apify-pp-cli digest from {{.SourceCount}} items in the local store._
`

const tiktokScriptTemplate = `[Hook — read first 3 seconds]
This week in {{.Topic}}, {{len .Items}} stories worth knowing.

[Body]
{{range $i, $it := .Items}}{{add $i 1}}. {{$it.Title}}
{{if $it.Body}}{{$it.Body}}
{{end}}
{{end}}
[Outro]
Follow for the weekly {{.Topic}} digest.
`

func newDigestCmd(flags *rootFlags) *cobra.Command {
	var (
		topic        string
		sinceStr     string
		template_    string
		templateFile string
		actorCSV     string
		limit        int
		offline      bool
	)

	cmd := &cobra.Command{
		Use:   "digest",
		Short: "Render a newsletter-ready digest from cached dataset items",
		Long: strings.Trim(`
Pull items from the local store within a time window, dedupe by URL +
title-similarity, rank by engagement + recency + novelty, and fill a
Go text/template. Output is markdown ready for Beehiiv/Ghost paste.

Built-in templates:
  default        markdown digest with bulleted links + bodies
  tiktok-script  hook + numbered list + outro for short-form video

Use --template-file <path> to supply your own Go text/template that
receives {Topic, Window, Items[], SourceCount} in its context.

With --offline, makes no API calls — runs entirely off the local store.

Examples:
  apify-pp-cli digest --topic AI --since 24h
  apify-pp-cli digest --topic "vision pro" --since 7d --template tiktok-script
  apify-pp-cli digest --offline --topic AI --since 30d --limit 10
  apify-pp-cli digest --topic agent --actors trudax/reddit-scraper --since 7d --json
`, "\n"),
		Example: strings.Trim(`
  apify-pp-cli digest --topic AI --since 24h --template default
  apify-pp-cli digest --topic "AI dev tools" --since 7d --template-file ./weekly.tmpl
  apify-pp-cli digest --offline --topic agent --since 30d --json
`, "\n"),
		Annotations: map[string]string{
			"mcp:read-only": "true",
		},
		RunE: func(cmd *cobra.Command, args []string) error {
			if dryRunOK(flags) {
				return nil
			}
			if topic == "" {
				return usageErr(fmt.Errorf("--topic is required"))
			}

			ctx := cmd.Context()
			db, err := store.OpenWithContext(ctx, defaultDBPath("apify-pp-cli"))
			if err != nil {
				return configErr(fmt.Errorf("opening local store: %w", err))
			}
			defer db.Close()
			if err := db.EnsureExtensions(ctx); err != nil {
				return configErr(fmt.Errorf("ensuring extensions: %w", err))
			}

			sinceDur := parseSinceWindow(sinceStr)
			if sinceDur == 0 {
				sinceDur = 7 * 24 * time.Hour
			}
			items, err := loadDigestItems(ctx, db, topic, sinceDur, splitCSV(actorCSV), limit)
			if err != nil {
				return apiErr(fmt.Errorf("loading items: %w", err))
			}

			ranked := dedupeAndRank(items)
			if limit > 0 && len(ranked) > limit {
				ranked = ranked[:limit]
			}

			// --offline asserts the digest must come from cached data. If the
			// local store has nothing for this topic, fail fast with an
			// actionable message instead of rendering an empty digest that
			// looks like a successful (but blank) result.
			if offline && len(ranked) == 0 {
				return notFoundErr(fmt.Errorf(
					"--offline: no cached items match topic %q in the last %s; run `apify-pp-cli run <actor>` or `apify-pp-cli sync` to populate the local store first",
					topic, prettyDuration(sinceDur)))
			}

			// JSON output: emit the structured items + meta
			if flags.asJSON {
				return printJSONFiltered(cmd.OutOrStdout(), map[string]any{
					"topic":  topic,
					"window": prettyDuration(sinceDur),
					"count":  len(ranked),
					"items":  ranked,
				}, flags)
			}

			// Otherwise render template
			tmplStr, err := resolveDigestTemplate(template_, templateFile)
			if err != nil {
				return usageErr(err)
			}
			return renderDigest(cmd.OutOrStdout(), tmplStr, topic, sinceDur, ranked, len(items))
		},
	}

	cmd.Flags().StringVar(&topic, "topic", "", "Topic (required) — filter items by FTS match against title+body")
	cmd.Flags().StringVar(&sinceStr, "since", "7d", "Time window (e.g. 24h, 7d, 30d)")
	cmd.Flags().StringVar(&template_, "template", "default", "Built-in template: default | tiktok-script")
	cmd.Flags().StringVar(&templateFile, "template-file", "", "Path to a Go text/template file (overrides --template)")
	cmd.Flags().StringVar(&actorCSV, "actors", "", "Restrict to a CSV of source actors")
	cmd.Flags().IntVar(&limit, "limit", 20, "Max items in the digest")
	cmd.Flags().BoolVar(&offline, "offline", false, "Require cached data: fail fast if the local store has no items for the topic (digest never calls the API regardless)")
	return cmd
}

// DigestItem is the per-item view passed to the template.
type DigestItem struct {
	Hash              string  `json:"hash"`
	URL               string  `json:"url,omitempty"`
	Title             string  `json:"title,omitempty"`
	Body              string  `json:"body,omitempty"`
	Author            string  `json:"author,omitempty"`
	PublishedAt       string  `json:"published_at,omitempty"`
	PublishedAtPretty string  `json:"published_at_pretty,omitempty"`
	SourceActor       string  `json:"source_actor"`
	Engagement        int64   `json:"engagement,omitempty"`
	Score             float64 `json:"score"`
}

// loadDigestItems pulls candidate items from pp_dataset_items.
// FTS match on the topic (title + body) within the time window.
func loadDigestItems(ctx context.Context, db *store.Store, topic string,
	since time.Duration, actors []string, limit int) ([]DigestItem, error) {
	ftsQuery := `"` + strings.ReplaceAll(topic, `"`, `""`) + `"`
	// Cap candidates at 5x the digest limit so dedupe + rank has room to work
	candidateLimit := limit * 5
	if candidateLimit < 100 {
		candidateLimit = 100
	}

	q := `SELECT i.hash, i.url, i.title, i.body, i.author, i.published_at,
	             i.source_actor, i.engagement_score
	      FROM pp_dataset_items i
	      JOIN pp_dataset_items_fts f ON f.rowid = i.rowid
	      WHERE pp_dataset_items_fts MATCH ?`
	args := []any{ftsQuery}

	if len(actors) > 0 {
		ph := strings.Repeat("?,", len(actors))
		ph = strings.TrimRight(ph, ",")
		q += ` AND i.source_actor IN (` + ph + `)`
		for _, a := range actors {
			args = append(args, a)
		}
	}
	if since > 0 {
		cutoff := time.Now().Add(-since).UTC().Format(time.RFC3339)
		q += ` AND i.fetched_at >= ?`
		args = append(args, cutoff)
	}
	q += fmt.Sprintf(` ORDER BY i.fetched_at DESC LIMIT %d`, candidateLimit)

	rows, err := db.DB().QueryContext(ctx, q, args...)
	if err != nil {
		return nil, err
	}
	defer rows.Close()

	var out []DigestItem
	for rows.Next() {
		var d DigestItem
		if err := rows.Scan(&d.Hash, &d.URL, &d.Title, &d.Body, &d.Author,
			&d.PublishedAt, &d.SourceActor, &d.Engagement); err != nil {
			return nil, err
		}
		d.PublishedAtPretty = prettifyPublishedAt(d.PublishedAt)
		out = append(out, d)
	}
	return out, rows.Err()
}

// dedupeAndRank removes items with the same URL or near-identical titles,
// then scores each by engagement + recency + novelty heuristics.
func dedupeAndRank(items []DigestItem) []DigestItem {
	seenURL := map[string]bool{}
	seenTitleHash := map[string]bool{}
	deduped := items[:0]
	for _, it := range items {
		// URL dedupe
		if it.URL != "" {
			if seenURL[it.URL] {
				continue
			}
			seenURL[it.URL] = true
		}
		// Title-similarity dedupe via first-5-word hash
		if it.Title != "" {
			th := titleSimHash(it.Title)
			if seenTitleHash[th] {
				continue
			}
			seenTitleHash[th] = true
		}
		deduped = append(deduped, it)
	}

	// Score: engagement (log-scale) + recency (closer to now = higher) + length bonus
	now := time.Now().UTC()
	for i := range deduped {
		it := &deduped[i]
		engagement := math.Log1p(float64(it.Engagement)) * 2
		recency := 0.0
		if it.PublishedAt != "" {
			if t, err := time.Parse(time.RFC3339, it.PublishedAt); err == nil {
				ageHours := now.Sub(t).Hours()
				if ageHours < 0 {
					ageHours = 0
				}
				recency = math.Max(0, 10-math.Log1p(ageHours))
			}
		}
		lengthBonus := 0.0
		if len(it.Body) > 200 {
			lengthBonus = 1.0
		}
		it.Score = engagement + recency + lengthBonus
	}

	sort.SliceStable(deduped, func(i, j int) bool {
		return deduped[i].Score > deduped[j].Score
	})
	return deduped
}

func titleSimHash(title string) string {
	// Lowercase, drop punctuation, first 5 words
	t := strings.ToLower(title)
	var b strings.Builder
	for _, r := range t {
		if (r >= 'a' && r <= 'z') || (r >= '0' && r <= '9') || r == ' ' {
			b.WriteRune(r)
		}
	}
	words := strings.Fields(b.String())
	if len(words) > 5 {
		words = words[:5]
	}
	return strings.Join(words, " ")
}

func resolveDigestTemplate(name, file string) (string, error) {
	if file != "" {
		data, err := os.ReadFile(file)
		if err != nil {
			return "", fmt.Errorf("reading template file %q: %w", file, err)
		}
		return string(data), nil
	}
	switch strings.ToLower(name) {
	case "", "default", "markdown":
		return defaultDigestTemplate, nil
	case "tiktok-script", "tiktok":
		return tiktokScriptTemplate, nil
	}
	return "", fmt.Errorf("unknown template %q (built-ins: default, tiktok-script)", name)
}

func renderDigest(w interface {
	Write(p []byte) (n int, err error)
}, tmplStr, topic string, since time.Duration, items []DigestItem, sourceCount int) error {
	funcs := template.FuncMap{
		"add": func(a, b int) int { return a + b },
	}
	tmpl, err := template.New("digest").Funcs(funcs).Parse(tmplStr)
	if err != nil {
		return fmt.Errorf("parsing template: %w", err)
	}
	return tmpl.Execute(w, map[string]any{
		"Topic":       topic,
		"Window":      prettyDuration(since),
		"Items":       items,
		"SourceCount": sourceCount,
	})
}

func prettyDuration(d time.Duration) string {
	if d == 0 {
		return "all-time"
	}
	switch {
	case d < time.Hour:
		return fmt.Sprintf("last %d min", int(d.Minutes()))
	case d < 24*time.Hour:
		return fmt.Sprintf("last %d hours", int(d.Hours()))
	case d < 30*24*time.Hour:
		return fmt.Sprintf("last %d days", int(d.Hours()/24))
	default:
		return fmt.Sprintf("last %d months", int(d.Hours()/24/30))
	}
}

func prettifyPublishedAt(s string) string {
	if s == "" {
		return ""
	}
	t, err := time.Parse(time.RFC3339, s)
	if err != nil {
		return s
	}
	return t.Format("Jan 2, 2006")
}

